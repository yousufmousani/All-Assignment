var num1 = +prompt('Enter No 1')
var num2 = +prompt('Enter No 2')
var operater = prompt('Enter Operater \n +\n - \n * \n / \n %')

if(operater == '+' ){
alert(num1+num2)
}else if(operater == '-' ){
    alert(num1-num2)
}else if(operater == '*' ){
    alert(num1*num2)
}else if(operater == '/' ){
    alert(num1/num2)
}else if(operater == '%' ){
        alert(num1%num2)
}


